package learn_JAVA;

public class Display {
    public static void main(String[] args) {
        /* 
         * คำสั่งแสดงผลข้อความบนหน้าจอ
         * System.out.print() - ปริ้นข้อความโดยไม่ขึ้นบรรทัดใหม่
         * System.out.println() - ปริ้นข้อความแล้วขึ้นบรรทัดใหม่
        */

        // ปริ้นข้อความโดยไม่ขึ้นบรรทัดใหม่
        System.out.print("Welcome to ");
        // ปริ้นข้อความแล้วขึ้นบรรทัดใหม่
        System.out.println("Display Class");
    }
}
