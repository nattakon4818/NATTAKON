package chapter02;
public class Comparison {
    public static void main(String[] args) {
        int x = 3, y = 6;
        System.out.println("x == y = " + (x == y));
        System.out.println("x != y = " + (x != y));
        System.out.println("x > y = " + (x > y));
        System.out.println("x >= y = " + (x >= y));
        System.out.println("x < y = " + (x < y));
        System.out.println("x <= y = " + (x <= y));
    }
}
